package day16_nestedLoop;

public class LoopPractices {
    public static void main(String[] args) {

        for (int i = 1; i < 6; i++) {
            System.out.println("Hello");
        }

        int j = 0;
        while(j < 5){

            System.out.println("Cydeo");
            j++;
        }

        int k = 0;

        do {
            System.out.println("Hello Cydeo");
            k++;
        }while (k< 5);


    }
}
