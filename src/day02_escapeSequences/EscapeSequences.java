package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");

        System.out.println("--------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Batch 1");

        System.out.println("--------------------------------");

        System.out.println("\\");

        System.out.println("---------------------------------");

        System.out.println("My favorite TV show is \"Friends\"");

        /*
            1.\n: new line
            2.\t: paragraph space
            3.\\: backslash
            4.\": quotes

         */

    }
}
